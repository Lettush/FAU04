// Variables = containers
/* 
    let - mutable/changeable value
    const - unmutable/unchangeable value
    var - same as let, just with a few scoping problems
*/

// let
let firstName = "Jared";
console.log("Firstname:", firstName);

// const
const lastName = "Hortaleza";
console.log("Lastname:", lastName);

// editing let
firstName = "Ariston";
console.log("Firstname:", firstName);

